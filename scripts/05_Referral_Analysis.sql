## Referral Programme Effectiveness

### Business Question

Did referred customers generate more business value than organically acquired customers?

> ** The referral program does not attract high-value customers than organically acquired customers, and here is why;
      - The combined revenue from customers acquired through one referrer is lower than the revenue generated by an individual organic customer. 
      - Only three referrers (C02,C167,C207) produced a total downstream referral revenue that exceeded the highest revenue generated by a non-referred customer (C271).**
